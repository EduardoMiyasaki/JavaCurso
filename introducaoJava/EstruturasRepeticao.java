public class EstruturasRepeticao {

    public static void main (String[] args) {

        // While e for em java

        int Contador = 5;

        for (int i = 0; i < Contador; i++) {
            System.out.println("Oi" + i);

        }

        int i = 0;
        while (i < Contador) {
            System.out.println("Tchau" + i);
            i++;

        }
    }
}
